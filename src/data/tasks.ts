export function getTaskById(id: string): Task | undefined {
  return TASKS.find((t) => t.id === id);
}

export function getTasksByCategory(cat: Category): Task[] {
  return TASKS.filter((t) => t.category === cat);
}

export function getTasksByType(type: TaskType): Task[] {
  return TASKS.filter((t) => t.type === type);
}

export function getTotalXp(task: Task): number {
  return task.mainXp + (task.subXp ?? 0);
}

export function calcCategoryScores(completedTaskIds: string[]): Record<Category, number> {
  const scores: Record<Category, number> = {
    strength: 0,
    sleep: 0,
    nutrition: 0,
    environment: 0,
    mental: 0,
  };
  for (const id of completedTaskIds) {
    const task = getTaskById(id);
    if (!task) continue;
    scores[task.category] += task.mainXp;
    if (task.subCategory && task.subXp) {
      scores[task.subCategory] += task.subXp;
    }
  }
  return scores;
}

export function calcTotalXp(completedTaskIds: string[]): number {
  const scores = calcCategoryScores(completedTaskIds);
  return Object.values(scores).reduce((a, b) => a + b, 0);
}
